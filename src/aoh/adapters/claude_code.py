"""Claude Code runtime adapter.

Materializes a self-contained workspace directory that Claude Code can be
launched against: skills, per-skill slash commands, a per-role subagent
file, CLAUDE.md project memory, a `settings.json` guardrail (permission
deny/allow lists + a fail-closed PreToolUse hook), and — for scoped
bindings — a provisioned read-only kubeconfig.

Threat model (see .planning/design/2026-07-16-claude-codex-adapters-design.md
§threat model): the HARD enforcement boundary is the scoped RBAC identity
provisioned by `provision.sh` (shared with the Hermes adapter via
`aoh.adapters._k8s`). `settings.json` permissions and the `kubectl-guard.sh`
hook are a BEST-EFFORT runtime guardrail layered on top — not a security
boundary. A hostile or confused agent could still bypass the guardrail
(e.g. by asking a human to run a command, or via any Claude Code escape
hatch this adapter doesn't know about); it cannot bypass cluster RBAC.
CLAUDE.md states this honestly so operators do not mistake the guardrail
for containment.
"""

from __future__ import annotations

import json
import os
import stat
from pathlib import Path
from shutil import copytree

from aoh.adapters._k8s import (
    KUBECTL_MUTATION_COMMANDS,
    KUBECTL_READ_COMMANDS,
    render_provision_script,
    validate_binding_fields,
)
from aoh.adapters.base import ADAPTERS, AdapterResult, MaterializeRequest
from aoh.pack import Binding, Pack, PackError, Role, load_role

__all__ = ["ClaudeCodeAdapter"]


_HELM_MUTATION_COMMANDS = ("install", "upgrade", "uninstall", "rollback")


def _kubectl_guard_script() -> str:
    """Render the fail-closed PreToolUse hook script.

    Contract (design doc, authoritative):
      - stdin is the tool-call JSON; `.tool_input.command` is the shell
        command Claude Code is about to run via the Bash tool.
      - jq missing, JSON parse failure, or an empty/absent command ->
        exit 2 (block). Never fall through to allow.
      - Strip leading wrapper tokens `sudo`, `env` (including `env`'s
        leading `VAR=value` assignments and short flags), `time`.
      - If the (wrapper-stripped) command is `sh -c '...'` or
        `bash -c "..."`, re-extract and re-normalize the inner command.
      - Strip a leading absolute path off the kubectl/helm token
        (e.g. `/usr/bin/kubectl` -> `kubectl`).
      - If the first token is not kubectl/helm, this hook has no opinion:
        exit 0.
      - Tokenize the remaining args, skipping over `--flag`, `--flag=v`,
        and `-n v` / `--context v` style pairs, to find the verb (and, for
        `auth`, the sub-verb) tuple. `kubectl auth can-i` is allowed;
        `kubectl auth reconcile` is a mutation and is blocked.
      - Verb (tuple-aware) in the read allowlist -> exit 0. Anything else
        kubectl/helm -> print a reason to stderr, exit 2.
      - Ambiguous or dangerous shell syntax (pipes `|`, command chaining
        `;`/`&`, redirects `>`/`<`, command substitution `$(...)` or
        backticks) anywhere in the raw command, or inside an unwrapped
        `sh -c`/`bash -c` inner command -> exit 2 (can't safely prove it's
        read-only, and these characters must never reach the tokenizer
        below, which uses a bash array assignment that would otherwise
        re-interpret them).
    """

    read_verbs = " ".join(KUBECTL_READ_COMMANDS)
    mutation_verbs = " ".join(KUBECTL_MUTATION_COMMANDS)

    return f'''#!/usr/bin/env bash
# Generated by AOH. PreToolUse guardrail for the Bash tool: fail-closed
# best-effort parser that blocks kubectl/helm mutation commands.
#
# THIS IS NOT A SECURITY BOUNDARY. It is a best-effort runtime guardrail
# layered on top of the scoped RBAC identity (the hard boundary). See
# CLAUDE.md in this workspace and the AOH design doc for the full threat
# model. Any parse ambiguity, missing dependency, or malformed input fails
# CLOSED (exit 2 / block) — never falls through to allow.

set -u

READ_VERBS="{read_verbs}"
MUTATION_VERBS="{mutation_verbs}"

block() {{
  echo "kubectl-guard: $1" >&2
  exit 2
}}

if ! command -v jq >/dev/null 2>&1; then
  block "jq is not available; failing closed"
fi

RAW_INPUT="$(command cat)"
if [ -z "${{RAW_INPUT}}" ]; then
  block "empty stdin; failing closed"
fi

if ! echo "${{RAW_INPUT}}" | jq -e . >/dev/null 2>&1; then
  block "stdin is not valid JSON; failing closed"
fi

COMMAND="$(echo "${{RAW_INPUT}}" | jq -r '.tool_input.command // empty' 2>/dev/null)"
if [ -z "${{COMMAND}}" ]; then
  block "no .tool_input.command in tool call; failing closed"
fi

# Reject any shell metacharacter that could chain, redirect, substitute, or
# otherwise change what "the command" means (pipes, `;`, `&`, redirects,
# command substitution, backticks). Fail closed on ANY of these regardless
# of whether kubectl/helm is mentioned — this also guarantees the tokenizer
# below (which uses a bash array assignment) never sees anything that could
# execute. Backslashes are rejected too: they can be used to smuggle
# metacharacters past a naive filter.
case "${{COMMAND}}" in
  *'|'*|*'&'*|*';'*|*'>'*|*'<'*|*'$('*|*'`'*|*'\\'*)
    block "command contains shell metacharacters (pipe/chain/redirect/substitution); failing closed"
    ;;
esac

tokenize() {{
  # Splits a metacharacter-free command string into TOKENS, honoring shell
  # word-splitting/quoting so `sh -c "kubectl delete pod x"` becomes ONE
  # inner token, not four. Safe here ONLY because the caller has already
  # rejected pipes/chaining/redirects/substitution/backticks/backslashes
  # above — no executable construct can survive into this eval.
  local input="$1"
  eval "TOKENS=(${{input}})" 2>/dev/null
}}

strip_wrappers() {{
  # Strips leading sudo/env/time wrapper tokens (may repeat), including
  # `env`'s leading VAR=value assignment arguments (e.g. `env FOO=bar
  # kubectl ...`) and short env flags like `-i`/`-u NAME`, so the wrapped
  # command isn't mistaken for a non-kubectl command.
  while [ "${{#TOKENS[@]}}" -gt 0 ]; do
    case "${{TOKENS[0]}}" in
      sudo|time)
        TOKENS=("${{TOKENS[@]:1}}")
        ;;
      env)
        TOKENS=("${{TOKENS[@]:1}}")
        while [ "${{#TOKENS[@]}}" -gt 0 ]; do
          case "${{TOKENS[0]}}" in
            *=*)
              TOKENS=("${{TOKENS[@]:1}}")
              ;;
            -u)
              TOKENS=("${{TOKENS[@]:2}}")
              ;;
            -*)
              TOKENS=("${{TOKENS[@]:1}}")
              ;;
            *)
              break
              ;;
          esac
        done
        ;;
      *)
        break
        ;;
    esac
  done
}}

tokenize "${{COMMAND}}"
if [ "${{#TOKENS[@]}}" -eq 0 ]; then
  block "command did not tokenize to anything; failing closed"
fi

strip_wrappers

if [ "${{#TOKENS[@]}}" -eq 0 ]; then
  block "empty command after normalization; failing closed"
fi

FIRST="${{TOKENS[0]}}"

# Unwrap `sh -c '...'` / `bash -c "..."` — re-extract the inner command and
# re-run normalization against it.
case "${{FIRST}}" in
  sh|bash|/bin/sh|/bin/bash|/usr/bin/sh|/usr/bin/bash)
    if [ "${{TOKENS[1]:-}}" = "-c" ] && [ -n "${{TOKENS[2]:-}}" ]; then
      INNER="${{TOKENS[2]}}"
      case "${{INNER}}" in
        *'|'*|*'&'*|*';'*|*'>'*|*'<'*|*'$('*|*'`'*|*'\\'*)
          block "sh/bash -c inner command contains shell metacharacters; failing closed"
          ;;
      esac
      tokenize "${{INNER}}"
      strip_wrappers
      if [ "${{#TOKENS[@]}}" -eq 0 ]; then
        block "empty inner command after normalization; failing closed"
      fi
      FIRST="${{TOKENS[0]}}"
    else
      # sh/bash without a clean -c '<command>' form — cannot safely parse.
      block "sh/bash wrapper without a plain -c '<command>' form; failing closed"
    fi
    ;;
esac

# Strip a leading absolute path off the kubectl/helm token.
BASENAME="${{FIRST##*/}}"

if [ "${{BASENAME}}" != "kubectl" ] && [ "${{BASENAME}}" != "helm" ]; then
  # Not our concern — no kubectl/helm invocation detected.
  exit 0
fi

TOOL="${{BASENAME}}"

# Walk remaining args, skipping global flags, to find the verb (and
# sub-verb, for `auth`) tuple.
VERB=""
SUBVERB=""
i=1
while [ "$i" -lt "${{#TOKENS[@]}}" ]; do
  tok="${{TOKENS[$i]}}"
  case "${{tok}}" in
    --*=*)
      i=$((i + 1))
      continue
      ;;
    -n|--namespace|--context|--kubeconfig|--cluster|--user)
      i=$((i + 2))
      continue
      ;;
    --*|-*)
      i=$((i + 1))
      continue
      ;;
    *)
      VERB="${{tok}}"
      i=$((i + 1))
      break
      ;;
  esac
done

if [ -z "${{VERB}}" ]; then
  block "${{TOOL}} invocation with no discernible verb; failing closed"
fi

if [ "${{TOOL}}" = "kubectl" ] && [ "${{VERB}}" = "auth" ]; then
  # Find the sub-verb (next non-flag token).
  while [ "$i" -lt "${{#TOKENS[@]}}" ]; do
    tok="${{TOKENS[$i]}}"
    case "${{tok}}" in
      --*=*)
        i=$((i + 1))
        continue
        ;;
      -n|--namespace|--context|--kubeconfig|--cluster|--user)
        i=$((i + 2))
        continue
        ;;
      --*|-*)
        i=$((i + 1))
        continue
        ;;
      *)
        SUBVERB="${{tok}}"
        break
        ;;
    esac
  done
  if [ "${{SUBVERB}}" = "can-i" ]; then
    exit 0
  fi
  block "${{TOOL}} auth ${{SUBVERB:-<none>}} is not in the read allowlist"
fi

if [ "${{TOOL}}" = "kubectl" ]; then
  for v in ${{READ_VERBS}}; do
    if [ "${{v}}" = "${{VERB}}" ]; then
      exit 0
    fi
  done
  for v in ${{MUTATION_VERBS}}; do
    if [ "${{v}}" = "${{VERB}}" ]; then
      block "kubectl ${{VERB}} is a mutation verb; blocked by guardrail"
    fi
  done
  block "kubectl ${{VERB}} is not in the read allowlist; failing closed"
fi

if [ "${{TOOL}}" = "helm" ]; then
  case "${{VERB}}" in
    install|upgrade|uninstall|rollback)
      block "helm ${{VERB}} is a mutation verb; blocked by guardrail"
      ;;
    *)
      exit 0
      ;;
  esac
fi

block "unrecognized command shape; failing closed"
'''


def _render_settings_json(*, workspace: Path) -> dict:
    kubeconfig_path = str((workspace / "kubeconfig").resolve())
    hook_path = str((workspace / ".claude" / "hooks" / "kubectl-guard.sh").resolve())

    deny = [f"Bash(kubectl {verb}:*)" for verb in KUBECTL_MUTATION_COMMANDS]
    deny.extend(f"Bash(helm {verb}:*)" for verb in _HELM_MUTATION_COMMANDS)

    allow = [f"Bash(kubectl {cmd}:*)" for cmd in KUBECTL_READ_COMMANDS]
    allow.append("Bash(./.claude/skills/*)")

    return {
        "env": {"KUBECONFIG": kubeconfig_path},
        "permissions": {
            "deny": deny,
            "allow": allow,
            "defaultMode": "default",
        },
        "hooks": {
            "PreToolUse": [
                {
                    "matcher": "Bash",
                    "hooks": [{"type": "command", "command": hook_path}],
                }
            ]
        },
    }


def _render_agent_md(role: Role) -> str:
    description = role.purpose or f"{role.display_name} role."
    responsibilities = "\n".join(f"- {item}" for item in role.responsibilities)
    return (
        "---\n"
        f"name: {role.name}\n"
        f"description: {description}\n"
        "---\n\n"
        f"# {role.display_name}\n\n"
        f"{role.purpose}\n\n"
        "## Responsibilities\n\n"
        f"{responsibilities}\n\n"
        "## Read-only contract\n\n"
        "You operate under a scoped, read-only Kubernetes identity (the hard "
        "enforcement boundary — cluster RBAC). A best-effort guardrail also "
        "blocks kubectl/helm mutation commands at the tool layer. Prefer "
        "read-only inspection; report denials as the guardrail working, not "
        "as errors to work around.\n"
    )


def _render_command(pack: Pack, skill: str) -> str:
    roles = ", ".join(pack.roles) or "the default Claude Code session"
    requirements = ", ".join(pack.runtime_requirements) or "the active runtime configuration"

    return (
        f"# ops:{skill}\n\n"
        f"Use the `{skill}` skill for this AOH command.\n\n"
        f"- AOH pack: `{pack.name}`\n"
        f"- Role: {roles}\n"
        f"- Runtime requirements: {requirements}\n\n"
        "Follow the skill instructions, stay focused, and report any runtime "
        "capability that Claude Code cannot satisfy directly.\n"
    )


def _render_claude_md(pack: Pack, *, role: Role | None, binding: Binding | None) -> str:
    title = role.display_name if role else pack.name
    skills = ", ".join(role.skills if role and role.skills else pack.skills)
    responsibilities = (
        "\n".join(f"- {item}" for item in role.responsibilities) if role else ""
    )

    binding_block = ""
    if binding is not None:
        namespace = binding.target.get("namespace", "default")
        binding_block = (
            "\n## Binding\n\n"
            f"- Bound cluster (kube context): {binding.target.get('kubeContext')}\n"
            f"- Default namespace: {namespace} (you may inspect other namespaces)\n"
            f"- Access mode: {binding.access}\n"
        )

    walls = (
        "\n## Read-only enforcement — read this before using kubectl/helm\n\n"
        "This workspace layers two controls. They are not the same thing and "
        "must not be confused:\n\n"
        "1. **Hard enforcement boundary — cluster RBAC.** The `kubeconfig` in "
        "this workspace authenticates as a scoped ServiceAccount bound to a "
        "read-only (get/list/watch) ClusterRole. The Kubernetes API server "
        "itself rejects mutating requests from this identity, regardless of "
        "what Claude Code or this workspace's configuration does. This is "
        "the boundary that actually holds.\n"
        "2. **Best-effort runtime guardrail — `.claude/settings.json` "
        "permissions plus the `kubectl-guard.sh` PreToolUse hook.** These "
        "deny kubectl/helm mutation commands at the tool layer before they "
        "run, as a fast, friendly backstop. This is convenience, not "
        "containment: it is scoped to commands Claude Code routes through "
        "its Bash tool, and a sufficiently motivated bypass (e.g. asking a "
        "human operator to run a command outside this session) is not "
        "something a runtime-level hook can stop. Treat guardrail denials "
        "as the system working as intended, not as bugs to route around.\n\n"
        "The scoped kubeconfig is the workspace's default identity, not a "
        "sandbox: it does not isolate other credentials that may be present "
        "on this host. Containment (isolated HOME or a container) is out of "
        "scope for this workspace.\n"
    )

    return (
        f"# AOH Claude Code workspace: {title}\n\n"
        "You are an Agentic Ops Harness runtime agent running under Claude "
        "Code. Stay focused on your assigned role and prefer the installed "
        "AOH skills over generic troubleshooting.\n\n"
        f"- Pack: {pack.name}\n"
        f"- Role: {role.name if role else '(none)'}\n"
        f"- Skills: {skills}\n"
        + (f"\n## Responsibilities\n\n{responsibilities}\n" if responsibilities else "")
        + binding_block
        + walls
    )


def _render_launch_script(*, with_kubeconfig: bool) -> str:
    kubeconfig_line = (
        'export KUBECONFIG="${DIR}/kubeconfig"\n' if with_kubeconfig else ""
    )
    return (
        "#!/usr/bin/env bash\n"
        "set -euo pipefail\n"
        'DIR="$(cd "$(dirname "$0")" && pwd)"\n'
        'cd "${DIR}"\n'
        f"{kubeconfig_line}"
        'exec claude "$@"\n'
    )


class ClaudeCodeAdapter:
    """RuntimeAdapter that materializes a Claude Code workspace directory."""

    name = "claude-code"

    def materialize(self, request: MaterializeRequest) -> AdapterResult:
        pack = request.pack
        binding = request.binding
        role_name = request.role_name

        if binding is not None:
            if binding.role not in pack.roles:
                raise PackError(
                    f"Binding `{binding.name}` references missing role `{binding.role}`"
                )
            if role_name is not None and role_name != binding.role:
                raise PackError(
                    f"Binding `{binding.name}` role `{binding.role}` conflicts with --role `{role_name}`"
                )
            if not binding.target.get("kubeContext"):
                raise PackError(
                    f"Binding `{binding.name}` target.kubeContext is required for kubernetes targets"
                )
            if binding.access == "inherit":
                raise PackError(
                    "access=inherit not yet supported by claude-code adapter"
                )
            validate_binding_fields(binding)
            role_name = binding.role

        workspace = Path(request.output_dir)
        claude_dir = workspace / ".claude"
        skills_dir = claude_dir / "skills"
        commands_dir = claude_dir / "commands" / "ops"
        agents_dir = claude_dir / "agents"
        hooks_dir = claude_dir / "hooks"

        for d in (skills_dir, commands_dir, agents_dir, hooks_dir):
            d.mkdir(parents=True, exist_ok=True)

        role = load_role(pack, role_name) if role_name else None
        selected_skills = role.skills if role and role.skills else pack.skills

        generated: list[Path] = []

        for skill in selected_skills:
            source = pack.root / "skills" / skill
            destination = skills_dir / skill
            copytree(source, destination, dirs_exist_ok=True)
            generated.append(destination / "SKILL.md")

            command_file = commands_dir / f"{skill}.md"
            command_file.write_text(_render_command(pack, skill), encoding="utf-8")
            generated.append(command_file)

        if role is not None:
            agent_file = agents_dir / f"{role.name}.md"
            agent_file.write_text(_render_agent_md(role), encoding="utf-8")
            generated.append(agent_file)

        hook_file = hooks_dir / "kubectl-guard.sh"
        hook_file.write_text(_kubectl_guard_script(), encoding="utf-8")
        os.chmod(
            hook_file,
            stat.S_IRWXU | stat.S_IRGRP | stat.S_IXGRP | stat.S_IROTH | stat.S_IXOTH,
        )
        generated.append(hook_file)

        settings_file = claude_dir / "settings.json"
        settings_file.write_text(
            json.dumps(_render_settings_json(workspace=workspace), indent=2, sort_keys=True)
            + "\n",
            encoding="utf-8",
        )
        generated.append(settings_file)

        claude_md_file = workspace / "CLAUDE.md"
        claude_md_file.write_text(
            _render_claude_md(pack, role=role, binding=binding), encoding="utf-8"
        )
        generated.append(claude_md_file)

        launch_file = workspace / "launch.sh"
        launch_file.write_text(
            _render_launch_script(with_kubeconfig=binding is not None), encoding="utf-8"
        )
        os.chmod(launch_file, 0o755)
        generated.append(launch_file)

        if binding is not None:
            provision_file = workspace / "provision.sh"
            provision_file.write_text(render_provision_script(binding), encoding="utf-8")
            os.chmod(provision_file, 0o755)
            generated.append(provision_file)

        return AdapterResult(runtime="claude-code", output_dir=workspace, generated_files=generated)


ADAPTERS["claude-code"] = ClaudeCodeAdapter()
